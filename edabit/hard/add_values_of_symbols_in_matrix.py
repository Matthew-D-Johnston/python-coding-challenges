def check_score(matrix):
    symbol_values = {
      '#': 5,
      'O': 3,
      'X': 1,
      '!': -1,
      '!!': -3,
      '!!!': -5
    }
    score = 0

    for row in matrix:
        for symbol in row:
            score += symbol_values[symbol]
    
    if score < 0:
        return 0
    else:
        return score

